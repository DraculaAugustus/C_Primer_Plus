#include <stdio.h>

int main(void)
{
	printf("interger division: 5/4 is %d\n", 5/4);
	printf("interger division: 6/3 is %d\n", 6/3);
	printf("interger division: 7/4 is %d\n", 7/4);

	printf("floating division: 7.0/4.0 is %.2f\n", 7.0/4.0);
	printf("mixed division: 7.0/4 is %.2f\n", 7.0/4);
	printf("-7/4 is %d\n", -7/4);

	return 0;
}
